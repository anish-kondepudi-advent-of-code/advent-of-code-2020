# Runs in O(n) - n is number of rows
def findTrees(forest):
	col = 0
	trees = 0
	for row in forest:
		if row[col%len(row)] == '#':
			trees += 1
		col += 3
	return trees

def productTreesSlopes(forest):
	product_trees = 1

	# Slope 1/1
	col = 0
	trees = 0
	for row in forest:
		if row[col%len(row)] == '#':
			trees += 1
		col += 1
	if trees != 0:
		product_trees *= trees

	# Slope 3/1
	col = 0
	trees = 0
	for row in forest:
		if row[col%len(row)] == '#':
			trees += 1
		col += 3
	if trees != 0:
		product_trees *= trees

	# Slope 5/1
	col = 0
	trees = 0
	for row in forest:
		if row[col%len(row)] == '#':
			trees += 1
		col += 5
	if trees != 0:
		product_trees *= trees

	# Slope 7/1
	col = 0
	trees = 0
	for row in forest:
		if row[col%len(row)] == '#':
			trees += 1
		col += 7
	if trees != 0:
		product_trees *= trees

	# Slope 1/2
	col = 0
	trees = 0
	row = 0
	while row < len(forest):
		if forest[row][col%len(forest[0])] == '#':
			trees += 1
		row += 2
		col += 1
	if trees != 0:
		product_trees *= trees

	return product_trees



